#include <bits/stdc++.h>
typedef long long ll;
using namespace std;

// The problem is quite easy, where the ends have two representations while the number of diamonds in the middle get to be added to it.
// So, results as the same value of n.

int main()
{
    ll t, n;
    cin >> t;
    while (t--)
    {
        cin >> n;
        cout << n << "\n";
    }
    return 0;
}